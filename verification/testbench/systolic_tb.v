`timescale 1ns/1ps

module systolic_array_2x2_tb;

parameter WIDTH = 8;

/////////////////////////////////////////////////////////
// Clock & Reset
/////////////////////////////////////////////////////////

reg clk;
reg rst;
reg en;
reg valid_in;

/////////////////////////////////////////////////////////
// Inputs
/////////////////////////////////////////////////////////

reg [WIDTH-1:0] activation_in0;
reg [WIDTH-1:0] activation_in1;

reg [WIDTH-1:0] weight_in0;
reg [WIDTH-1:0] weight_in1;

/////////////////////////////////////////////////////////
// Outputs
/////////////////////////////////////////////////////////

wire [(2*WIDTH)-1:0] partial_sum00;
wire [(2*WIDTH)-1:0] partial_sum01;
wire [(2*WIDTH)-1:0] partial_sum10;
wire [(2*WIDTH)-1:0] partial_sum11;

wire valid_out;

/////////////////////////////////////////////////////////
// DUT
/////////////////////////////////////////////////////////

systolic_array_2x2 #(
    .WIDTH(WIDTH)
)
dut
(
    .clk(clk),
    .rst(rst),
    .en(en),

    .valid_in(valid_in),

    .activation_in0(activation_in0),
    .activation_in1(activation_in1),

    .weight_in0(weight_in0),
    .weight_in1(weight_in1),

    .partial_sum00(partial_sum00),
    .partial_sum01(partial_sum01),
    .partial_sum10(partial_sum10),
    .partial_sum11(partial_sum11),

    .valid_out(valid_out)
);

/////////////////////////////////////////////////////////
// Clock Generation
/////////////////////////////////////////////////////////

always #5 clk = ~clk;

/////////////////////////////////////////////////////////
// Dumpfile
/////////////////////////////////////////////////////////

initial begin

    $dumpfile("./verification/waveform/systolic_array_2x2.vcd");
    $dumpvars(0,dut);

end

/////////////////////////////////////////////////////////
// Monitor
/////////////////////////////////////////////////////////

initial
begin

$monitor(
"Time=%0t | V=%b | A0=%d A1=%d | W0=%d W1=%d || SUM00=%d SUM01=%d SUM10=%d SUM11=%d",

$time,

valid_out,

activation_in0,
activation_in1,

weight_in0,
weight_in1,

partial_sum00,
partial_sum01,
partial_sum10,
partial_sum11

);

end

/////////////////////////////////////////////////////////
// Stimulus
/////////////////////////////////////////////////////////

initial
begin

clk = 0;
rst = 1;
en = 0;
valid_in = 0;

activation_in0 = 0;
activation_in1 = 0;

weight_in0 = 0;
weight_in1 = 0;

/////////////////////////////////////////////////////////
// Reset
/////////////////////////////////////////////////////////

#20;

rst = 0;
en = 1;
valid_in = 1;

/////////////////////////////////////////////////////////
// Cycle 1
/////////////////////////////////////////////////////////

activation_in0 = 2;
activation_in1 = 4;

weight_in0 = 3;
weight_in1 = 5;

#10;

/////////////////////////////////////////////////////////
// Cycle 2
/////////////////////////////////////////////////////////

activation_in0 = 6;
activation_in1 = 8;

weight_in0 = 7;
weight_in1 = 9;

#10;

/////////////////////////////////////////////////////////
// Cycle 3
/////////////////////////////////////////////////////////

activation_in0 = 10;
activation_in1 = 12;

weight_in0 = 11;
weight_in1 = 13;

#10;

/////////////////////////////////////////////////////////
// Stop Injecting
/////////////////////////////////////////////////////////

valid_in = 0;

#100;

$finish;

end

endmodule